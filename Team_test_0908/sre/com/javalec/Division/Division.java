package sre.com.javalec.Division;

public class Division {

	int x_num=0;
	int y_num=0;
	int z_num=0;
	
	public Division(int x_num,int y_num, int z_num) {
		super();
		this.x_num=x_num;
		this.y_num=y_num;
		this.z_num=z_num;
		
		
		
	}
	
	public int Division() {
		int i=x_num/z_num;
		
		return i;
	}
	
}
